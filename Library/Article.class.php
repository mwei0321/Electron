    namespace Library;

    class Article {

        function __construct(){

        }

        function getArtList($_where = [],$_limit = 'count'){
            $m = M('Article');
            if($_limit == 'count'){
                $count = $m->where($_where)->count();
                return $count;
            }
            $list = $m->where($_where)->order('uptime DESC')->select();
            return $list;
        }
    }